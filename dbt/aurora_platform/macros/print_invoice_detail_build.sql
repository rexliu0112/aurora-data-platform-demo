{% macro print_invoice_detail_build(company)%}

SELECT
    ROW_NUMBER() OVER (ORDER BY CT011_13 ASC) AS SNO,
    CONCAT(CT011_1, CT011_2, CT011_3) AS CTNO,
    CT011_11 AS CUSTNO,
    TRIM(CT011_8) AS MARA,
    TRIM(CT011_4) AS MACNO,
    CT011_5 AS CREATE_NO,
    CT011_13 AS CREATED_DT,
    SUBSTR(CT011_63, 1, 6) AS INVOICE_DT_M,
    SUBSTR(CT011_13, 1, 6) AS CREATED_DT_M,
    CT011_63 AS INVOICE_DT,
    PARSE_DATE('%Y%m%d', CT011_63) AS INVOICE_DATE,
    CT011_62 AS INVOICE_NUMBER,         -- 發票號碼
    CT011_65 AS RETURN_DT,
    CT011_66 AS RETURN_ID,
    CT011_21 AS BLACK_COUNTER,          -- 本次計數器 (黑) A4-抄表數
    CT011_22 AS COLOR_COUNTER,          -- 本次計數器 (彩) A4-抄表數
    CT011_25 AS BLACK_QUANTITY,
    CT011_26 AS COLOR_QUANTITY,
    CT011_AMT AS PRE_TAX_AMT,
    CURRENT_DATE() AS ETL_DT
FROM {{ ref('stg_' ~ company ~ '_ct011dl') }}
WHERE
    (CT011_65 = '' OR CT011_65 IS NULL)
    AND CT011_63 IS NOT NULL
    AND CT011_63 != ''
    AND CT011_63 <= FORMAT_DATE('%Y%m%d', CURRENT_DATE()) 

{% endmacro %}